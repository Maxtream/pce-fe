CREATE TABLE `participants` (
  `id` int(10) unsigned NOT NULL AUTO_INCREMENT,
  `user_id` int(10) unsigned NOT NULL DEFAULT '0',
  `team_id` int(10) unsigned NOT NULL DEFAULT '0',
  `game` varchar(10) NOT NULL,
  `server` varchar(5) DEFAULT NULL,
  `tournament_id` smallint(5) unsigned NOT NULL,
  `timestamp` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  `ip` varchar(20) DEFAULT NULL,
  `name` varchar(60) NOT NULL,
  `email` varchar(255) NOT NULL,
  `contact_info` text NOT NULL,
  `cpt_player_id` bigint(20) unsigned NOT NULL DEFAULT '0',
  `challonge_id` int(10) unsigned NOT NULL DEFAULT '0',
  `link` varchar(32) DEFAULT NULL,
  `approved` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `deleted` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `online` int(10) unsigned NOT NULL DEFAULT '0',
  `place` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `seed_number` smallint(4) unsigned NOT NULL DEFAULT '0',
  `ended` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `checked_in` tinyint(1) unsigned NOT NULL DEFAULT '0',
  `verified` tinyint(1) unsigned NOT NULL DEFAULT '0',
  PRIMARY KEY (`id`),
  KEY `tournament_id` (`tournament_id`),
  KEY `game` (`game`),
  KEY `server` (`server`),
  KEY `user_id` (`user_id`),
  KEY `team_id` (`team_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8