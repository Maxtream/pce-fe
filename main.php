        <div class="content" id="format-content">
            <h1>Format</h1>
            <ul>
                <li>EUW server</li>
                <li>Games starts <strong>1 february 2014</strong></li>
                <li>Min teams: 16</li>
                <li>Max teams: 128</li>
                <li>Anyone can participate if they are allowed by League of Legend rules</li>
                <li>Single elimination, Best of 1. Finals - Best of 3</li>
                <li>Prize - 50€ and Triumphant Ryze skin for 1st place. RP for 1st - 4th places.</li>
                <li>Few games will be transmited on twitch (no commentary) and VOD available on youtube.</li>
                <li>Quarter final, semi finals and finals will be broadcasted online, on twitch, with commentary</li>
            </ul>
            
            <h1>Rules</h1>
            <p>1. Регистрация:</p>
            <p>1.1 К участию допускаются команды прошедшие регистрацию.</p>
            <p>1.1.1 Имеющие возможность участвовать в режиме Tournament Draft (Выбор для турнира).</p>
            <p>1.1.2 Согласные с данными правилами.</p>
            <p>1.1.3 Не имеющие в составе команды игроков дисквалифицированных на турнире.</p>
            <p>1.2 Регистрация будет открыта за две недели до начала турнира. Пожалуйста, оставляйте заявки только с уверенностью, что сможете принять участие в турнирах.</p>
            <p>1.3 Отменить регистрацию можно за день до начала турнира. Для этого необходимо сообщить об этом организаторам турнира в скайп или удалив заявку через ссылку предоставленную в Email письме при регистрации.</p>
            <br />
            <p>2. Замены:</p>
            <p>2.1 Во время турнира, перед каждой игрой, можно свободно использовать замены указанные в заявке.</p>
            <p>2.2 Участие других, не указанных в заявке игроков запрещены и засчитываются как автоматическое поражение.</p>
            <p>2.3 В случае замены, приз турнира (RP + Skin) получают те участники команды, которые участвовали в финальных матчах.</p>
            <p>2.4 Замена игрока на весь турнира возможна только до quarter finals боёв и с предупреждением за 12 часов до следующего боя.</p>
            <br />
            <p>3. Пред турнирная подготовка:</p>
            <p>3.1 Организатор чемпионата (PentaClick eSports) создает сетку (brackets).</p>
            <p>3.2 Команды не попавшие в сетку не участвуют на турнире, это может возникнуть по следующей причине:</p>
            <p>3.2.1 В составе команды был обнаружен игрок, дисквалифицированный на турнирах.</p>
            <p>3.2.2 Не хватка места обусловленная ограничением сетке (16, 32, 64, 128).</p>
            <p>3.2.3 В названии команды содержится оскорбление, выражение запрещенное цензурой, выражение направленное на разжигание межнациональной розни или иные, задевающие национальность/вероисповедание игроков.</p>
            <br />
            <p>4. Подготовка:</p>
            <p>4.1 Капитаны договариваються о времени проведения матча в общем скайп-чате PentaClick eSports. Участники приглашают свою команду и команду соперника в Custom Game в режиме Tournament Draft. Так же, при определённой договорённости приглашаются организаторы, в качестве зрителей.</p>
            <p>4.2(Выбор для турниров):</p>
            <p>4.2.1 Приглашает команда, которая находится сверху в сетке.</p>
            <p>4.2.2 Приглашение осуществляется через капитана команды.</p>
            <br />
            <p>5. Время проведения:</p>
            <p>5.1 На сбор команды после назначеного времени даёться 15 минут.</p>
            <p>5.2 Игра стартует несмотря на недостающих игроков и протекает как любая другая игра.</p>
            <p>5.3 Команда в которой игрок не явился в течении 15 минут, получает дисквалификацию. Исключение составляют проблемы клиента (требуется информирование организатора).</p>
            <br />
            <p>6. Завершение:</p>
            <p>6.1 Результат матча, нотификация приходит организаторам автоматически.</p>
            <p>6.2 Организатор турнира после получения оповещения вводит результат в сетку, и победившая команда ждет следующего соперника.</p>
            <br />
            <p>7. Формат проведения стадий:</p>
            <p>7.1 Представляет собой систему матчей на вылет (плей-офф). Все игры проходят в формате Bo1 (до одной победы).</p>
            <p>7.2 Финальная стадия турнира проводится в системе Bo3 (до двух побед).</p>
            <br />
            <p>8. Формат проведения матчей:</p>
            <p>8.1 Создается Custom game (своя игра) в режиме Tournament Draft (Выбор для турниров).</p>
            <p>8.2 Карта турнира: Summoner`s Rift (Ущелье призывателей).</p>
            <p>8.3 Побеждает команда, которая первой уничтожит вражеский нексус.</p>
            <br />
            <p>9. Запрещенные приемы:</p>
            <p>9.1 Использование ошибок игры.</p>
            <p>9.2 Использование стороннего программного обеспечения.</p>
            <p>9.3 использование, каких-либо программ, дающих преимущество перед другими участниками соревнований.</p>
            <p>9.4 Частые перезаходы игрока во время проведения боя будут рассматриваться, как использования ошибок игры в некорректных целях (искусственно созданный лаг) после двух предупреждений игрок получает дисквалификацию.</p>
            <p>9.5 Использование любых программ/скриптов.</p>
            <p>9.6 Использование багов карт/клиента/сервера.</p>
            <p>9.7 Запрещается использовать в названии Своей игры (Custom game) мат, оскорбление, бранную лексику, рекламу, выражение запрещенное цензурой, выражение направленное на разжигание межнацианальной розни или иные выражения, задеваюшие национальность/вероисповедание игроков.</p>

            <h1>Additional information</h1>
            <ul>
                <li>Тот, кто находится выше в сетке должен заинвайтить оппонентов в течении 15 минут.</li>
                <li>Игроки обязаны присутствовать на турнире под своими игровыми именами, под которыми были заявлены на турнир. Смена ника до начала турнира будет рассчитыватся как не верный игрок команды, об этом нужно предупреждать Организатора за ранее согласно пункту 2.4.</li>
                <li>Организатор оставляет за собой право изменять и дополнять настоящие правила.</li>
                <li>После окончания капитаны команд занявшие 1, 2, 3 и 4 места должны связаться с Организатором.</li>
            </ul>
